def report_bounds(name, writer):
    xs = [vertex[0] for vertex in writer.vertices]
    ys = [vertex[1] for vertex in writer.vertices]
    zs = [vertex[2] for vertex in writer.vertices]
    print(
        f"{name}: {len(writer.vertices)} vertices, "
        f"bounds X {min(xs):.3f}..{max(xs):.3f}, "
        f"Y {min(ys):.3f}..{max(ys):.3f}, "
        f"Z {min(zs):.3f}..{max(zs):.3f}"
    )


