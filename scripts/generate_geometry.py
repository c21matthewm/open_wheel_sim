#!/usr/bin/env python3
"""Generate the sim's lightweight IR-18-inspired OBJ assets.

Coordinates match the renderer convention:
  X = lateral, Y = up, Z = forward.
"""

import math
from pathlib import Path


ASSET_DIR = Path("assets/meshes")


def add(a, b):
    return (a[0] + b[0], a[1] + b[1], a[2] + b[2])


def sub(a, b):
    return (a[0] - b[0], a[1] - b[1], a[2] - b[2])


def mul(a, scalar):
    return (a[0] * scalar, a[1] * scalar, a[2] * scalar)


def dot(a, b):
    return a[0] * b[0] + a[1] * b[1] + a[2] * b[2]


def cross(a, b):
    return (
        a[1] * b[2] - a[2] * b[1],
        a[2] * b[0] - a[0] * b[2],
        a[0] * b[1] - a[1] * b[0],
    )


def length(a):
    return math.sqrt(max(dot(a, a), 0.0))


def normalize(a):
    mag = length(a)
    if mag < 1.0e-6:
        return (0.0, 1.0, 0.0)
    return (a[0] / mag, a[1] / mag, a[2] / mag)


def face_normal(points):
    if len(points) < 3:
        return (0.0, 1.0, 0.0)
    return normalize(cross(sub(points[2], points[0]), sub(points[1], points[0])))


class OBJWriter:
    def __init__(self, title):
        self.title = title
        self.vertices = []
        self.normals = []
        self.faces = []
        self.current_material = None

    def add_vertex(self, point):
        self.vertices.append(point)
        return len(self.vertices)

    def add_normal(self, normal):
        self.normals.append(normalize(normal))
        return len(self.normals)

    def set_material(self, material):
        self.current_material = material

    def add_face(self, points, material, normals=None):
        if len(points) < 3:
            return
        self.set_material(material)
        if normals is None:
            normal_index = self.add_normal(face_normal(points))
            normal_indices = [normal_index for _ in points]
        else:
            normal_indices = [self.add_normal(normal) for normal in normals]
        vertex_indices = [self.add_vertex(point) for point in points]
        self.faces.append((vertex_indices, normal_indices, self.current_material))

    def add_tri(self, a, b, c, material, normal=None):
        normals = None if normal is None else [normal, normal, normal]
        self.add_face([a, b, c], material, normals)

    def add_quad(self, a, b, c, d, material, normal=None):
        normals = None if normal is None else [normal, normal, normal, normal]
        self.add_face([a, b, c, d], material, normals)

    def write(self, path):
        path.parent.mkdir(parents=True, exist_ok=True)
        with path.open("w", encoding="utf-8") as file:
            file.write(f"# {self.title}\n")
            file.write("# Generated by scripts/generate_geometry.py\n")
            for vertex in self.vertices:
                file.write(f"v {vertex[0]:.6f} {vertex[1]:.6f} {vertex[2]:.6f}\n")
            for normal in self.normals:
                file.write(f"vn {normal[0]:.6f} {normal[1]:.6f} {normal[2]:.6f}\n")

            last_material = None
            for vertex_indices, normal_indices, material in self.faces:
                if material != last_material:
                    file.write(f"usemtl {material}\n")
                    last_material = material
                tokens = [
                    f"{vertex_index}//{normal_index}"
                    for vertex_index, normal_index in zip(vertex_indices, normal_indices)
                ]
                file.write(f"f {' '.join(tokens)}\n")


def add_box(writer, min_point, max_point, material):
    x0, y0, z0 = min_point
    x1, y1, z1 = max_point
    writer.add_quad((x0, y0, z0), (x0, y1, z0), (x1, y1, z0), (x1, y0, z0), material, (0, 0, -1))
    writer.add_quad((x1, y0, z1), (x1, y1, z1), (x0, y1, z1), (x0, y0, z1), material, (0, 0, 1))
    writer.add_quad((x0, y0, z1), (x0, y1, z1), (x0, y1, z0), (x0, y0, z0), material, (-1, 0, 0))
    writer.add_quad((x1, y0, z0), (x1, y1, z0), (x1, y1, z1), (x1, y0, z1), material, (1, 0, 0))
    writer.add_quad((x0, y0, z1), (x0, y0, z0), (x1, y0, z0), (x1, y0, z1), material, (0, -1, 0))
    writer.add_quad((x0, y1, z0), (x0, y1, z1), (x1, y1, z1), (x1, y1, z0), material, (0, 1, 0))


def add_rect_loft(writer, sections, material):
    """Loft rectangular sections. Each section has z, cx, half_width, y_min, y_max."""
    rings = []
    for section in sections:
        z = section["z"]
        cx = section.get("cx", 0.0)
        half_width = section["half_width"]
        y_min = section["y_min"]
        y_max = section["y_max"]
        rings.append(
            [
                (cx - half_width, y_min, z),
                (cx + half_width, y_min, z),
                (cx + half_width, y_max, z),
                (cx - half_width, y_max, z),
            ]
        )

    for index in range(len(rings) - 1):
        a = rings[index]
        b = rings[index + 1]
        writer.add_quad(a[0], b[0], b[1], a[1], material)
        writer.add_quad(a[1], b[1], b[2], a[2], material)
        writer.add_quad(a[2], b[2], b[3], a[3], material)
        writer.add_quad(a[3], b[3], b[0], a[0], material)

    first = rings[0]
    last = rings[-1]
    writer.add_quad(first[0], first[1], first[2], first[3], material)
    writer.add_quad(last[3], last[2], last[1], last[0], material)


def add_ellipse_loft(writer, sections, material, segments=36, cap=True):
    rings = []
    normals = []
    for section in sections:
        z = section["z"]
        cx = section.get("cx", 0.0)
        cy = section["cy"]
        rx = section["rx"]
        ry = section["ry"]
        ring = []
        normal_ring = []
        for index in range(segments):
            angle = 2.0 * math.pi * index / segments
            ca = math.cos(angle)
            sa = math.sin(angle)
            ring.append((cx + rx * ca, cy + ry * sa, z))
            normal_ring.append(normalize((ca / max(rx, 0.001), sa / max(ry, 0.001), 0.0)))
        rings.append(ring)
        normals.append(normal_ring)

    for section_index in range(len(rings) - 1):
        for index in range(segments):
            next_index = (index + 1) % segments
            writer.add_face(
                [
                    rings[section_index][index],
                    rings[section_index + 1][index],
                    rings[section_index + 1][next_index],
                    rings[section_index][next_index],
                ],
                material,
                [
                    normals[section_index][index],
                    normals[section_index + 1][index],
                    normals[section_index + 1][next_index],
                    normals[section_index][next_index],
                ],
            )

    if cap:
        rear_center = (sections[0].get("cx", 0.0), sections[0]["cy"], sections[0]["z"])
        front_center = (sections[-1].get("cx", 0.0), sections[-1]["cy"], sections[-1]["z"])
        for index in range(segments):
            next_index = (index + 1) % segments
            writer.add_tri(rear_center, rings[0][next_index], rings[0][index], material, (0, 0, -1))
            writer.add_tri(front_center, rings[-1][index], rings[-1][next_index], material, (0, 0, 1))


def add_trapezoid_plate_x(writer, x_center, thickness, yz_points, material):
    x0 = x_center - thickness * 0.5
    x1 = x_center + thickness * 0.5
    left = [(x0, y, z) for y, z in yz_points]
    right = [(x1, y, z) for y, z in yz_points]
    writer.add_face(left, material, [(-1, 0, 0) for _ in left])
    writer.add_face(list(reversed(right)), material, [(1, 0, 0) for _ in right])
    for index in range(len(yz_points)):
        next_index = (index + 1) % len(yz_points)
        writer.add_quad(left[index], left[next_index], right[next_index], right[index], material)


def add_airfoil(writer, z_rear, z_front, half_span_rear, half_span_front, y_rear, y_front, thickness, material):
    bottom_offset = -thickness * 0.45
    top_offset = thickness * 0.55
    sections = [
        {
            "z": z_rear,
            "cx": 0.0,
            "half_width": half_span_rear,
            "y_min": y_rear + bottom_offset,
            "y_max": y_rear + top_offset,
        },
        {
            "z": z_front,
            "cx": 0.0,
            "half_width": half_span_front,
            "y_min": y_front + bottom_offset,
            "y_max": y_front + top_offset,
        },
    ]
    add_rect_loft(writer, sections, material)


def add_cylinder_between(writer, start, end, radius, material, segments=10):
    direction = sub(end, start)
    axis = normalize(direction)
    if length(direction) < 1.0e-5:
        return
    reference = (0.0, 1.0, 0.0)
    if abs(dot(axis, reference)) > 0.92:
        reference = (1.0, 0.0, 0.0)
    u_axis = normalize(cross(reference, axis))
    v_axis = normalize(cross(axis, u_axis))

    start_ring = []
    end_ring = []
    normals = []
    for index in range(segments):
        angle = 2.0 * math.pi * index / segments
        radial = add(mul(u_axis, math.cos(angle)), mul(v_axis, math.sin(angle)))
        start_ring.append(add(start, mul(radial, radius)))
        end_ring.append(add(end, mul(radial, radius)))
        normals.append(radial)

    for index in range(segments):
        next_index = (index + 1) % segments
        writer.add_face(
            [start_ring[index], end_ring[index], end_ring[next_index], start_ring[next_index]],
            material,
            [normals[index], normals[index], normals[next_index], normals[next_index]],
        )

    for index in range(segments):
        next_index = (index + 1) % segments
        writer.add_tri(start, start_ring[next_index], start_ring[index], material, mul(axis, -1.0))
        writer.add_tri(end, end_ring[index], end_ring[next_index], material, axis)


def add_revolved_profile_x(writer, profile, material, segments=64):
    rings = []
    normal_rings = []
    for profile_index, (x, radius) in enumerate(profile):
        ring = []
        normal_ring = []
        prev_x, prev_r = profile[max(0, profile_index - 1)]
        next_x, next_r = profile[min(len(profile) - 1, profile_index + 1)]
        slope_x = next_x - prev_x
        slope_r = next_r - prev_r
        for index in range(segments):
            angle = 2.0 * math.pi * index / segments
            sa = math.sin(angle)
            ca = math.cos(angle)
            ring.append((x, radius * sa, radius * ca))
            normal_ring.append(normalize((-slope_r, slope_x * sa, slope_x * ca)))
        rings.append(ring)
        normal_rings.append(normal_ring)

    for profile_index in range(len(profile) - 1):
        for index in range(segments):
            next_index = (index + 1) % segments
            writer.add_face(
                [
                    rings[profile_index][index],
                    rings[profile_index + 1][index],
                    rings[profile_index + 1][next_index],
                    rings[profile_index][next_index],
                ],
                material,
                [
                    normal_rings[profile_index][index],
                    normal_rings[profile_index + 1][index],
                    normal_rings[profile_index + 1][next_index],
                    normal_rings[profile_index][next_index],
                ],
            )


def add_cylinder_x(writer, width, radius, material, segments=64):
    half_width = width * 0.5
    profile = [(-half_width, 0.0), (-half_width, radius), (half_width, radius), (half_width, 0.0)]
    add_revolved_profile_x(writer, profile, material, segments)


def add_steering_arc(writer, radius_x, radius_y, start_degrees, end_degrees, material, radius=0.018, segments=18):
    last = None
    for index in range(segments + 1):
        t = index / segments
        angle = math.radians(start_degrees + (end_degrees - start_degrees) * t)
        point = (math.cos(angle) * radius_x, math.sin(angle) * radius_y, 0.0)
        if last is not None:
            add_cylinder_between(writer, last, point, radius, material, 8)
        last = point


