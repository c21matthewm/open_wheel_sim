def build_wheel() -> Mesh:
    mesh = Mesh()
    segments = 96
    width_profile = [
        (-0.30, 0.300),
        (-0.27, 0.345),
        (-0.22, 0.378),
        (-0.12, 0.392),
        (0.00, 0.398),
        (0.12, 0.392),
        (0.22, 0.378),
        (0.27, 0.345),
        (0.30, 0.300),
    ]
    rings = []
    for x, radius in width_profile:
        ring = []
        radius_dx = x / 0.30 * 0.42
        for index in range(segments):
            theta = math.tau * index / segments
            position = (x, math.cos(theta) * radius, math.sin(theta) * radius)
            normal = norm((radius_dx, math.cos(theta), math.sin(theta)))
            ring.append((position, normal))
        rings.append(ring)
    for ring_index in range(len(rings) - 1):
        for index in range(segments):
            next_index = (index + 1) % segments
            a, na = rings[ring_index][index]
            b, nb = rings[ring_index + 1][index]
            c, nc = rings[ring_index + 1][next_index]
            d, nd = rings[ring_index][next_index]
            mesh.quad("tire_rubber", a, b, c, d, (na, nb, nc, nd))

    cylinder_between(mesh, "rim_metal_lip", (-0.245, 0.0, 0.0), (-0.185, 0.0, 0.0), 0.258, 64)
    cylinder_between(mesh, "rim_metal_lip", (0.185, 0.0, 0.0), (0.245, 0.0, 0.0), 0.258, 64)
    cylinder_between(mesh, "rim_metal_inner_barrel", (-0.185, 0.0, 0.0), (0.185, 0.0, 0.0), 0.208, 64)
    cylinder_between(mesh, "brake_disc_metal", (-0.035, 0.0, 0.0), (0.035, 0.0, 0.0), 0.215, 64)
    cylinder_between(mesh, "hub_metal_center", (-0.075, 0.0, 0.0), (0.075, 0.0, 0.0), 0.112, 48)
    cylinder_between(mesh, "wheel_nut_metal", (-0.115, 0.0, 0.0), (0.115, 0.0, 0.0), 0.055, 6)

    for index in range(10):
        theta = math.tau * index / 10
        for offset in (-0.026, 0.026):
            radial = (0.0, math.cos(theta + offset), math.sin(theta + offset))
            start = (0.085, radial[1] * 0.11, radial[2] * 0.11)
            end = (0.225, radial[1] * 0.245, radial[2] * 0.245)
            cylinder_between(mesh, "spoke_metal_dual", start, end, 0.018, 10)
    for slot in range(18):
        theta = math.tau * slot / 18
        y = math.cos(theta) * 0.165
        z = math.sin(theta) * 0.165
        box(mesh, "brake_disc_slot", (-0.040, y - 0.010, z - 0.026), (0.040, y + 0.010, z + 0.026))
    box(mesh, "caliper_metal_red", (0.11, 0.145, -0.095), (0.26, 0.255, 0.105))
    return mesh


